'use strict'

/**
 * Multiplies two numbers
 * @param {number} a the first number
 * @param {number} b the second number
 * @return {number} the product of `a` and `b`
 * @public
 */
function multiply(a, b) {
  return a * b
}

module.exports = multiply;
